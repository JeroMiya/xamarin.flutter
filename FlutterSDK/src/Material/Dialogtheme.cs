        {
            this.BackgroundColor = backgroundColor;
            this.Elevation = elevation;
            this.Shape = shape;
            this.TitleTextStyle = titleTextStyle;
            this.ContentTextStyle = contentTextStyle;
        }
        /// <Summary>
        /// Default value for [Dialog.backgroundColor].
